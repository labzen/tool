package cn.labzen.tool.definition

/**
 * 数字特殊含义
 */
@Suppress("unused")
enum class Numbers {

  /**
   * 0
   */
  ZERO,

  /**
   * π
   */
  PI,

  /**
   * 整数
   */
  INTEGER,

  /**
   * 小数
   */
  DECIMAL,

  /**
   * 正数
   */
  POSITIVE_NUMBER,

  /**
   * 负数
   */
  NEGATIVE_NUMBER,

  /**
   * 偶数
   */
  EVEN_NUMBER,

  /**
   * 基数
   */
  ODD_NUMBER,

  /**
   * 素数，质数
   */
  PRIME_NUMBER
}
