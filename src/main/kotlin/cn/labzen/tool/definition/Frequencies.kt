package cn.labzen.tool.definition

/**
 * 频率，频繁程度（主观评定）
 */
@Suppress("unused")
enum class Frequencies {

  /**
   * 极端的频繁
   */
  EXTREMELY,

  /**
   * 经常的
   */
  OFTEN,

  /**
   * 定期的，有规律的
   */
  REGULAR,

  /**
   * 偶然的
   */
  CASUAL,

  /**
   * 几乎不
   */
  SCARCELY,

  /**
   * 不确定的
   */
  UNCLEAR
}
